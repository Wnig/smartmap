		var oNext = getByClass(oPic,'next-btn')[0];
		var aLi = oPic.getElementsByTagName('li');
		var arr = [];
		for(var i = 0; i < aLi.length; i++) {
		 	arr.push([parseInt(getStyle(aLi[i],'left')), parseInt(getStyle(aLi[i],'top'))]);
		}
		oPrev.onclick = function moveTP() {
		    arr.push(arr[0]);
			arr.shift();
